function traverse(start, graph, path) {
	const currentPath = [...path, start];
	if (currentPath.length > 40) {
		return currentPath;
	}

	let possibleMoves = getPossibleMoves(start, graph);

	for (let move of possibleMoves) {
		if (isFinish(move, graph)) {
			return currentPath
		}

		const finalPath = traverse(move, graph, currentPath);
		if (finalPath) {
			return finalPath;
		}
	}
}

function isFinish(tile, graph) {
	return graph[tile[1]][tile[0]] === 3;
}

function getPossibleMoves(currentState, graph, speed = 1) {
	let x = currentState[0];
	let y = currentState[1];

	let possibleMoves = [];
	let nx, ny;
	let diff = 1;

	// top left
	// nx = x - diff;
	// ny = y - diff;
	// addValid(nx, ny);

	// top
	nx = x;
	ny = y - diff;
	addValid(nx, ny);

	// top right
	nx = x;
	ny = y - diff;
	addValid(nx, ny);

	// right
	nx = x + diff;
	ny = y;
	addValid(nx, ny);

	// bottom right
	nx = x + diff;
	ny = y + diff;
	addValid(nx, ny);

	// bottom
	nx = x;
	ny = y + diff;
	addValid(nx, ny);

	// bottom left
	// nx = x - diff;
	// ny = y + diff;
	// addValid(nx, ny);

	// left
	// nx = x - diff;
	// ny = y;
	// addValid(nx, ny);

	return possibleMoves;

	function addValid(px, py) {
		if (isValid(px, py)) {
			possibleMoves.push([px, py])
		}
	}

	function isValid(px, py, width = 40, height = 30) {
		const outOfBounds = px < 0 || py < 0 || px >= width || py >= height;

		if (outOfBounds) {
			return false;
		}

		let tile = graph[py][px];

		return tile != 0 && tile != 2;

	};
}

function setPath(graph, path) {
	for (let tile of path) {
		graph[tile[1]][tile[0]] = 5;
	}
}

function print(graph) {
	let result = "";
	for (let i = 0; i < graph.length; i++) {
		result += JSON.stringify(graph[i]) + "\n";
	}
	console.log(result);
}

function getGraph(data) {
	let grid = data.grid;
	let {width, height} = data;
	const graph = [];
	let i, j;
	for(i = 0; i < height; i++) {
		for(j = 0; j < width; j++) {
			graph[i] = graph[i] || [];
			graph[i].push(grid[i * width + j]);
		}
	}
	return graph;
};

